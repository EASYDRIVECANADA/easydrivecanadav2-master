  add column if not exists suggested_vehicle_ids text[] not null default '{}'::text[],
  add column if not exists selected_vehicle_ids text[] not null default '{}'::text[],
  add column if not exists staff_note text,
  add column if not exists unlocked_at timestamptz,
  add column if not exists view_expires_at timestamptz,
  add column if not exists submitted_at timestamptz,
  add column if not exists expires_at timestamptz not null default (now() + interval '24 hours');

create unique index if not exists edc_fleet_quote_profiles_public_token_idx
  on public.edc_fleet_quote_profiles (public_token);

create index if not exists edc_fleet_quote_profiles_created_at_idx
  on public.edc_fleet_quote_profiles (created_at desc);

create index if not exists edc_fleet_quote_profiles_submitted_at_idx
  on public.edc_fleet_quote_profiles (submitted_at desc)
  where submitted_at is not null;

