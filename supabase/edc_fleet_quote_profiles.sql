create table if not exists public.edc_fleet_quote_profiles (
  id uuid primary key default gen_random_uuid(),
  public_token uuid not null default gen_random_uuid(),
  customer_name text not null,
  customer_phone_last4 text not null,
  passcode_hash text not null,
  province text not null default 'ON',
  apr numeric not null default 0.0799,
  term_months integer not null default 96,
  warranty_tier text not null default '3yr',
  max_selling_price numeric,
  max_monthly_payment numeric,
  max_biweekly_payment numeric,
  hide_disqualified boolean not null default false,
  suggested_vehicle_ids text[] not null default '{}'::text[],
  selected_vehicle_ids text[] not null default '{}'::text[],
  staff_note text,
  unlocked_at timestamptz,
  view_expires_at timestamptz,
  submitted_at timestamptz,
  expires_at timestamptz not null default (now() + interval '24 hours'),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.edc_fleet_quote_profiles
